#include "philo.h"

int	f_atoi(char *s)
{
	long		res;

	res = 0;
	while (*s && (*s == 32 || (*s <= 9 && *s >= 13)))
		s++;
	if (*s == '\0')
		return (-1);
	if (*s == '+' || *s == '-')
	{
		if (*s == '-')
			return (-1);
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		res = res * 10 + *s - '0';
		s++;
		if (res > 2147483647)
			return (-1);
	}
	while (*s && (*s == 32 || (*s <= 9 && *s >= 13)))
		s++;
	if (*s != '\0' || res == 0)
		return (-1);
	return (res);
}
